alert(8 % 3); // остаток от деления
alert(3 ** 2); // возведение в степень
alert(4 ** (1 / 2)); // квадратный корень
alert(8 ** (1 / 3)); // кубический корень

// оператор + может складывать разные типы данных
let str = "it's my" + "life"; // it's my life
alert(str);
alert(1 + 2); // 3
alert('1' + 2); // 12
alert(2 + '1'); // 21
alert(2 + 2 + '1') // 41

// остальные арифметические операторы стремятся преобразовать операнды в числа
alert(6 - '1'); // 5